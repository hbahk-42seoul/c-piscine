void	ft_rev_int_tab(int *tab, int size)
{
	int tab_[size];
	int i;

	i = 0;
	while (i < size)
	{
		tab_[i] = tab[i];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = tab_[size - 1 - i];
		i++;
	}
}
