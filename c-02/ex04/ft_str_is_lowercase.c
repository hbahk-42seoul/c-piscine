int		ft_str_is_lowercase(char *str)
{
	int i;
	int flag;

	i = 0;
	flag = 1;
	if (*str == '\0')
		return (1);
	while (*(str + i) && flag)
	{
		if (*(str + i) >= 'a' && *(str + i) <= 'z')
		{
			flag = 1;
			i++;
		}
		else
			flag = 0;
	}
	return (flag);
}
