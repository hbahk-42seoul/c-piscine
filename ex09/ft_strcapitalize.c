char	*ft_strcapitalize(char str[])
{
	int		i;
	char	a;

	i = 0;
	while (str[i])
	{
		if (i > 0)
			a = str[i - 1];
		if (a >= '0' && a <='9'
				|| a >= 'A' && a <= 'Z'
				|| a >= 'a' && a <= 'z')
			str[i] = str[i] + ('a' - 'A');
		else
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = str[i] - ('a' - 'A');
		}
		i++;
	}
	return (str);
}
